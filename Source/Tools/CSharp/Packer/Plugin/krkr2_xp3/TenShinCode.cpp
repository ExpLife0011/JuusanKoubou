Bool CTenshin::GenerateBody(UInt32 uLoop)
{
    if (--uLoop == 0)
    {
        return GenerateTail();
    }

    AddBytes(0x53);

    if (GenerateSeed() & 1)
    {
        if (GenerateBody(uLoop) == False)
            return False;
    }
    else if (GenerateBody2(uLoop) == False)
        return False;

    AddBytes(0x89, 0xC3);

    if (GenerateSeed() & 1)
    {
        if (GenerateBody(uLoop) == False)
            return False;
    }
    else if (GenerateBody2(uLoop) == False)
        return False;

    UInt32 mod = GenerateSeed() % 6;
    switch (mod)
    {
        case 2:
            AddBytes(0x29, 0xD8);
            break;

        case 0:
        case 1:
            AddBytes(0x51);
            AddBytes(0x89, 0xD9);
            AddBytes(0x83, 0xE1, 0x0F);
            AddBytes(0xD3, mod == 0 ? 0xE0 : 0xE8);
            AddBytes(0x59);
            break;

        case 3:
            AddBytes(0x0F, 0xAF, 0xC3);
            break;

        case 4:
            AddBytes(0xF7, 0xD8);

        case 5:
            AddBytes(0x01, 0xD8);
            break;
    }

    AddBytes(0x5B);
    return True;
}

Bool CTenshin::GenerateBody2(UInt32 uLoop)
{
    if (--uLoop == 0)
        return GenerateTail();

    if (GenerateSeed() & 1)
    {
        if (GenerateBody(uLoop) == False)
            return False;
    }
    else if (GenerateBody2(uLoop) == False)
        return False;

    switch (GenerateSeed() & 7)
    {
        case 1:
            AddBytes(0xF7, 0xD8);
            break;

        case 0:
            AddBytes(0xBE);
            AddDword(m_EncryptBlock);
            AddBytes(0x25);
            AddDword(0x3FF);
            AddBytes(0x8B, 0x04, 0x86);
            break;

        case 2:
            AddBytes(0x40);
            break;

        case 7:
            AddBytes(0xF7, 0xD0);
            break;

        case 5:
            AddBytes(0x48);
            break;

        case 3:
            AddBytes(0x53);
            AddBytes(0x89, 0xC3);
            AddBytes(0x81, 0xE3, 0xAA, 0xAA, 0xAA, 0xAA);
            AddBytes(0x25, 0x55, 0x55, 0x55, 0x55);
            AddBytes(0xD1, 0xEB);
            AddBytes(0xD1, 0xE0);
            AddBytes(0x09, 0xD8);
            AddBytes(0x5B);
            break;

        case 4:
            AddBytes(0x35);
            AddDword(GenerateSeed());
            break;

        case 6:
            AddBytes((GenerateSeed() & 1) ? 0x05 : 0x2D);
            AddDword(GenerateSeed());
            break;
    }

    return True;
}

Bool CTenshin::GenerateTail()
{
    UInt32 mod = GenerateSeed() % 3;

    switch (mod)
    {
        case 2:
            AddBytes(0xBE);
            AddDword(m_EncryptBlock);
            AddBytes(0x8B, 0x86);
            AddDword((GenerateSeed() & 0x3FF) << 2);
            break;

        case 1:
            AddBytes(0xB8);
            AddDword(GenerateSeed());
            break;

        case 0:
            AddBytes(0x8B, 0xC7);
            break;
    }

    return True;
}

